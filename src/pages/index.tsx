
import Head from "next/head";

import { api } from "~/utils/api";

import { useRouter } from "next/router";
import Layout from "~/components/Layout";

import { type NextPage } from "next";
import { LoadingMessage,ErrorMessage } from "~/components/ErrorMessage";
import { MuliColumnVideo } from "~/components/VideoComponent"
import HeroSection from "~/components/Herosection";
import { Input } from "@nextui-org/react";
import { SearchIcon } from "lucide-react";
import React, { useState, type ChangeEvent, type KeyboardEvent } from "react";

const Home: NextPage = () => {
  const router = useRouter();
  

  const { data, isLoading, error} = api.video.getRandomVideos.useQuery(30);

  const [searchInput, setSearchInput] = useState("");


  const handleSearch = async () => {
    try {
      await router.push({
        pathname: "/SearchPage",
        query: { q: searchInput },
      });
    } catch (error) {
      console.error("Error navigating to search page:", error);
    }
  };

  const handleKeyDown = (e: KeyboardEvent<HTMLInputElement>) => {
    if (e.key === "Enter") {
      void handleSearch();
    }
  };
  

  const Error = () => {
    if (isLoading) {
      return (
        <div className="mx-auto grid grid-cols-1 gap-x-10 gap-y-8 md:mx-0 md:max-w-none md:grid-cols-2 lg:mx-0 lg:max-w-none lg:grid-cols-3 xl:mx-0 xl:max-w-none xl:grid-cols-3 2xl:mx-0 2xl:max-w-none 2xl:grid-cols-3 items-center justify-center gap-4 py-8 md:py-10">
      <LoadingMessage />
      <LoadingMessage />
      <LoadingMessage />
      <LoadingMessage />
      <LoadingMessage />
      <LoadingMessage />
      
      </div>
      );
    } else if (error ?? !data) {
      return (
        <ErrorMessage
          icon="GreenPlay"
          message="No Videos"
          description="Sorry there is no videos at this time."
        />
      );
    } else {
      return <></>;
    }
  };

  return (
    <>
      <Head>
        <title>Gimbals.io</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout>

      
     
      <section className="flex flex-col items-center justify-center gap-4">
      <HeroSection/>
      <div className="flex sm:w-[740px] w-[340px]  px-2 sm:px-8 rounded-2xl flex-col justify-center items-center ">
  <Input
    classNames={{
      base: "w-full sm:w-3/4 h-10",
      mainWrapper: "h-full",
      input: "text-small",
      inputWrapper:
        "h-full font-normal text-default-500 bg-default-400/20 dark:bg-default-500/20",
    }}
    placeholder="Type to search..."
    size="lg"
    startContent={<SearchIcon size={18} />}
    type="search"
    onChange={(e: ChangeEvent<HTMLInputElement>) =>
      setSearchInput(e.target.value)
    }
    onKeyDown={handleKeyDown}
  />
</div>
        <div className="p-10">
        {!data ? (
          <Error />
        ) : (
          <>
               <MuliColumnVideo
              videos={data.videos.map((video) => ({
                id: video?.id ?? "",
                title: video?.title ?? "",
                thumbnailUrl: video?.thumbnailUrl ?? "",
                createdAt: video?.createdAt ?? new Date(),
                views: video?.views ?? 0,
              }))}
              users={data.users.map((user) => ({
                name: user?.name ?? "",
                image: user?.image ?? "",
              }))}
            />
            </>
            )}
        </div>
        
      </section>
      </Layout>
      
    </>
  );
}


export default Home;